"""
Executable entry point to run evaluations and output baseline metrics.

Updated 2026-05-10: Enhanced output with token/cost tracking and CLI flags.
"""

import os
import argparse
from datetime import datetime
from rich.console import Console

from src.agents.evals.model_comparison_harness import run_categorization_eval
from src.utils.path_utils import load_env_vars, get_project_root

console = Console()

def main():
    """Run baseline evaluation and save results to markdown.

    Supports --gemini-only and --groq-only flags to limit scope.
    """
    root = get_project_root()

    parser = argparse.ArgumentParser(description="Agentic Porter Baseline Evaluation")
    parser.add_argument("--gemini-only", action="store_true", help="Only test Tier 1 Gemini API models")
    parser.add_argument("--groq-only", action="store_true", help="Only test baseline Groq models")
    args = parser.parse_args()

    load_env_vars()

    # Determine which models to include
    include_gemini = True
    include_groq = True

    if args.gemini_only:
        include_groq = False
        console.print("[bold cyan]Mode: Gemini Tier 1 only[/bold cyan]")
    elif args.groq_only:
        include_gemini = False
        console.print("[bold cyan]Mode: Groq baselines only[/bold cyan]")
    else:
        console.print("[bold cyan]Mode: Full sweep (Groq + Gemini Tier 1)[/bold cyan]")

    results = run_categorization_eval(
        include_gemini=include_gemini,
        include_groq=include_groq,
    )

    current_date = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    out_dir = os.path.join(root, "data", "evals")
    os.makedirs(out_dir, exist_ok=True)
    out_file = os.path.join(out_dir, "baseline_results.md")

    with open(out_file, "w") as f:
        f.write("# Agentic Personal Porter - Baseline Evaluation\n")
        f.write(f"**Date:** {current_date}\n\n")

        # Summary table
        f.write("## Categorization Harness Results\n\n")
        f.write("| Provider | Model | Accuracy | Avg Latency | Tokens (In/Out) | Est. Cost |\n")
        f.write("|---|---|---|---|---|---|\n")
        for r in results:
            tokens_in = r.get("total_input_tokens", "?")
            tokens_out = r.get("total_output_tokens", "?")
            f.write(
                f"| {r['provider']} | {r['model']} | {r['accuracy']} | "
                f"{r['avg_latency']} | {tokens_in}/{tokens_out} | "
                f"{r['total_cost_usd']} |\n"
            )

        # Error details if any
        has_errors = any(r.get("errors") for r in results)
        if has_errors:
            f.write("\n## Errors\n\n")
            for r in results:
                if r.get("errors"):
                    f.write(f"### {r['provider']} / {r['model']}\n")
                    for err in r["errors"]:
                        f.write(f"- {err}\n")
                    f.write("\n")

        # Metadata
        f.write("\n---\n")
        f.write(f"*Generated by `run_baseline.py` on {current_date}*\n")

    console.print(f"\n[bold green]Baseline results saved to {out_file}[/bold green]")

if __name__ == "__main__":
    main()
